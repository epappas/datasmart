-module(files_server).
-author("epappas").

-include("file_records.hrl").

-behaviour(gen_server).

%% API
-export([
  start_link/0,
  fetch_info/3
]).

%% gen_server callbacks
-export([
  init/1,
  handle_call/3,
  handle_cast/2,
  handle_info/2,
  terminate/2,
  code_change/3
]).

-define(SERVER, ?MODULE).

-record(state, {}).

%%%===================================================================
%%% API
%%%===================================================================

start_link() ->
  gen_server:start_link({local, ?SERVER}, ?MODULE, [], []).

fetch_info(AuthKeyType, AuthKey, FileKey) ->
  gen_server:call(?MODULE, {fetch_info, {AuthKeyType, AuthKey, FileKey}}).

%%%===================================================================
%%% gen_server callbacks
%%%===================================================================

-spec(init(Args :: term()) ->
  {ok, State :: #state{}} | {ok, State :: #state{}, timeout() | hibernate} |
  {stop, Reason :: term()} | ignore).
init([]) ->
  {ok, #state{}}.

-spec(handle_call(Request :: term(), From :: {pid(), Tag :: term()},
    State :: #state{}) ->
  {reply, Reply :: term(), NewState :: #state{}} |
  {reply, Reply :: term(), NewState :: #state{}, timeout() | hibernate} |
  {noreply, NewState :: #state{}} |
  {noreply, NewState :: #state{}, timeout() | hibernate} |
  {stop, Reason :: term(), Reply :: term(), NewState :: #state{}} |
  {stop, Reason :: term(), NewState :: #state{}}).

handle_call({fetch_info, {AuthKeyType, AuthKey, FileKey}}, _From, State) ->
  {reply, doFetchInfo(AuthKeyType, AuthKey, FileKey), State};

handle_call(_Request, _From, State) ->
  {reply, {}, State}.

handle_cast(_Request, State) ->
  {noreply, State}.

handle_info(_Info, State) ->
  {noreply, State}.

terminate(_Reason, _State) ->
  ok.

code_change(_OldVsn, State, _Extra) ->
  {ok, State}.

%%%===================================================================
%%% Internal functions
%%%===================================================================

doFetchInfo(_AuthKeyType, _AuthKey, FileKey) ->
  case couch:get(?couch_file_uploads, FileKey) of
    {ok, FileJson} ->
      {FileKVList} = FileJson,
      {ok, FileKVList};
    _ -> {error, not_found}
  end.